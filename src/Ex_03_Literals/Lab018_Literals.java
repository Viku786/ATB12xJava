package Ex_03_Literals;

public class Lab018_Literals {
    public static void main(String[] args) {
        int age=65; //Decimal System - base will 10

        //Binary Literals
        int binary_number=0b1010; // Decimal system=10
        //0b is starting of binary number literals

        //Octal base-> 8
        int octal_number=01010;
        //0 is starting of octal number

        //Hexadecimal Literals ->16
        int hexa=0xface;
        //0x is starting of hexadecimal literals
        //base 16 color combination are in hex - #28a745
    }
}
