package Ex_04_Operators;

public class Lab028_Interview_concat_plus {
    public static void main(String[] args) {

        String s1="Vikash";
        String s2="Singh";

        int a=10;
        int b=50;

        System.out.println(s1+s2+a+b);
        System.out.println(a+b+s1+s2);
        System.out.println(s1+s2+(a+b));
    }
}
