package Ex_04_Operators;

public class Lab025_Relational_operator {
    public static void main(String[] args) {
        /* < -> smaller than
        <= -> smaller than equal to

        All of them result boolean output
         */

        int a=10;
        int b=20;
        boolean c=a>b;
        System.out.println(c);

        int c1=30, d=40;
        System.out.println(d>c1);
    }
}
