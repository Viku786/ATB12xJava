package Ex_04_Operators;

public class Lab022_Arithmetic_operator {
    public static void main(String[] args) {
         /* Arithmatic Operator
         +(Addition)
         -(Subtraction)
         *(Multiply)
         /(Division)
         %(Modulus)->Remainder
          */

        int a=10,b=3;
        float c=3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(a/c);
    }
}
