package Ex_10_For_Loop;

public class Lab_088_For_If_loop {
    public static void main(String[] args) {

        for (int i=0 ; i<5 ;i++){
            if(i>3){
                System.out.println("You get a gift");
            }
            else {
                System.out.println("You do not get a gift");
            }
        }
    }
}
